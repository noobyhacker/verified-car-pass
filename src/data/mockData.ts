// Helpers
export function getCarDataBySlug(slug: string) {
  const inspection = inspections.find((i) => i.public_slug === slug);
  if (!inspection) return null;
  const vehicle = vehicles.find((v) => v.vin === inspection.vin);
  const certificate = certificates.find((c) => c.inspection_id === inspection.id);
  const carMedia = media.filter((m) => m.vin === inspection.vin).sort((a, b) => a.sort_order - b.sort_order);
  const translationsForInspection = inspectionTranslations.filter((t) => t.inspection_id === inspection.id);
  const options = vehicleOptionsData[inspection.vin] || {};
  return { vehicle, inspection, certificate, media: carMedia, translations: translationsForInspection, options };
}

export function getInspectedVehicles() {
  return inspections
    .filter((ins) => ins.admin_status === "approved")
    .map((ins) => {
      const vehicle = vehicles.find((v) => v.vin === ins.vin);
      const cert = certificates.find((c) => c.inspection_id === ins.id);
      const photo = media.find((m) => m.vin === ins.vin && m.type === "image");
      return { inspection: ins, vehicle, cert, photo };
    })
    .filter((r) => r.vehicle && r.cert);
}

export function getUninspectedVehicles() {
  return vehicles
    .filter((v) => v.status === "pending")
    .map((vehicle) => {
      const photo = media.find((m) => m.vin === vehicle.vin && m.type === "image");
      const pendingInspection = inspections.find((i) => i.vin === vehicle.vin && i.admin_status === "pending");
      return { vehicle, photo, pendingInspection };
    });
}

export function getPendingReviews() {
  return inspections
    .filter((ins) => ins.admin_status === "pending")
    .map((ins) => {
      const vehicle = vehicles.find((v) => v.vin === ins.vin);
      const photo = media.find((m) => m.vin === ins.vin && m.type === "image");
      return { inspection: ins, vehicle, photo };
    })
    .filter((r) => r.vehicle);
}

export function getTrendingVehicles() {
  return vehicles
    .filter((v) => v.trending && v.status === "active")
    .map((vehicle) => {
      const inspection = inspections.find((i) => i.vin === vehicle.vin && i.admin_status === "approved");
      const cert = inspection ? certificates.find((c) => c.inspection_id === inspection.id) : null;
      const photo = media.find((m) => m.vin === vehicle.vin && m.type === "image");
      return { vehicle, inspection, cert, photo };
    })
    .filter((r) => r.inspection && r.cert);
}

export function formatPrice(priceUsd: number, currency: string): string {
  const rate = currencyRates[currency];
  if (!rate) return `$${priceUsd.toLocaleString()}`;
  const converted = Math.round(priceUsd * rate.rate);
  return `${rate.symbol}${converted.toLocaleString()}`;
}

